var wordsWithA = [];
var wordsNoA = [];
var wordsDisplay = [];

function organize() {
  for (let index = 0; index < dictionary.length; index++) {
    const w = dictionary[index];
    if (w.includes("a")) {
      wordsWithA.push(w.toLowerCase());
    } else {
      wordsNoA.push(w.toLowerCase());
    }
  }
  return [wordsWithA, wordsNoA];
}

function getWords(n, nA) {
  let randomNoA = wordsNoA.sort(() => 0.5 - Math.random()).slice(0, n - nA);
  let randomA = wordsWithA.sort(() => 0.5 - Math.random()).slice(0, nA);
  wordsDisplay = randomA.concat(randomNoA);

  return wordsDisplay.sort(() => 0.5 - Math.random());
}

